import UIKit

extension UIColor {
    class var htwDarkBlueGrey: UIColor {
        return UIColor(red: 28.0 / 255.0, green: 46.0 / 255.0, blue: 51.0 / 255.0, alpha: 1.0)
    }
    
    class var htwMainActive: UIColor {
        return UIColor(red:0.243, green:0.781, blue:1, alpha:1)
    }
    
    class var htwMainActiveDisabled: UIColor {
        return UIColor(red:0.12, green:0.296, blue:0.362, alpha:1)
    }
    
}
